const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

export default function App() {
  const [query, setQuery] = useState("");

  const [watched, setWatched] = useLocalStorageState([], "watched");

  const [selectedId, setSelectedId] = useState(null);

  function handleSelectMovie(id) {
    setSelectedId((sId) => (id === sId ? null : id));
  }

  function handleAddWatched(movie) {
    setWatched((wm) => [...wm, movie]);
  }

  function handleRemoveWatchedMovieById(id) {
    setWatched((wm) => wm.filter((movie) => movie.imdbID !== id));
  }

  // usign custom hook
  const { movies, isLoading, error } = useMovies(query, setSelectedId);

  return (
    <>
      <NavPar>
        <Logo />
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavPar>
      <Main>
        <Box>
          {isLoading && <Loader />}{" "}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onSelectMovie={handleSelectMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
              key={selectedId}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleRemoveWatchedMovieById}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}
